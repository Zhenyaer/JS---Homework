"use strict"

/**
 * Функция складывает 2 числа.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function addition (a, b) {
    return a + b;
}

/**
 * Функция вычитает 2 числа.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
 function subtraction (a, b) {
    return a - b;
}

/**
 * Функция умножает 2 числа.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
 function multiplication (a, b) {
    return a * b;
}

/**
 * Функция делит первое число на второе.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
 function divixion (a, b) {
    return a / b;
}